package pack1;



public class School {
	private String schoolName;
	private int numberOfStudents;
	private String schoolBoardName;
	private int numberOfTeachingStaff;

	
	public int getNumberOfTeachingStaff() {
		return numberOfTeachingStaff;
	}
	public void setNumberOfTeachingStaff(int numberOfTeachingStaff) {
		this.numberOfTeachingStaff = numberOfTeachingStaff;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public int getNumberOfStudents() {
		return numberOfStudents;
	}
	public void setNumberOfStudents(int numberOfStudents) {
		this.numberOfStudents = numberOfStudents;
	}
	public String getSchoolBoardName() {
		return schoolBoardName;
	}
	public void setSchoolBoardName(String schoolBoardName) {
		this.schoolBoardName = schoolBoardName;
	}
	
   
}
