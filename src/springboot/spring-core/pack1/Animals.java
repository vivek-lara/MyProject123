package pack1;

public class Animals {
	private String Cow;
	private String buffalo;
	private int numberOfCows;
	private int numberOfBuffalos;
	public String getCow() {
		return Cow;
	}
	public void setCow(String cow) {
		Cow = cow;
	}
	public String getBuffalo() {
		return buffalo;
	}
	public void setBuffalo(String buffalo) {
		this.buffalo = buffalo;
	}
	public int getNumberOfCows() {
		return numberOfCows;
	}
	public void setNumberOfCows(int numberOfCows) {
		this.numberOfCows = numberOfCows;
	}
	public int getNumberOfBuffalos() {
		return numberOfBuffalos;
	}
	public void setNumberOfBuffalos(int numberOfBuffalos) {
		this.numberOfBuffalos = numberOfBuffalos;
	}

}
